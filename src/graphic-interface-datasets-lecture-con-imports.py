# Funci칩n para cargar archivos
def load_data(file_path):
    try:
        if file_path.endswith(".csv"):
            df = pd.read_csv(file_path)
        elif file_path.endswith((".xls", ".xlsx")):
            df = pd.read_excel(file_path)
        elif file_path.endswith((".sqlite", ".db")):
            conn = sqlite3.connect(file_path)
            cursor = conn.cursor()
            cursor.execute("SELECT name FROM sqlite_master WHERE type='table';")
            tables = cursor.fetchall()
            if not tables:
                raise ValueError("No se encontraron tablas en la base de datos SQLite.")
            table_name = tables[0][0]
            df = pd.read_sql_query(f"SELECT * FROM {table_name}", conn)
            conn.close()
        else:
            raise ValueError("Formato de archivo no v치lido.")
        return df

    except FileNotFoundError:
        messagebox.showerror("Error", "Archivo no encontrado.")
    except pd.errors.EmptyDataError:
        messagebox.showerror("Error", "El archivo est치 vac칤o.")
    except sqlite3.OperationalError:
        messagebox.showerror("Error", "No se pudo leer la base de datos SQLite.")
    except ValueError as e:
        messagebox.showerror("Error", str(e))
    except Exception as e:
        messagebox.showerror("Error inesperado", f"Ocurri칩 un problema:\n{e}")
    return None


# Interfaz gr치fica (Tkinter)
def abrir_archivo():
    ruta = filedialog.askopenfilename(
        title="Seleccionar archivo",
        filetypes=[
            ("Archivos soportados", "*.csv *.xls *.xlsx *.sqlite *.db"),
            ("Todos los archivos", ".")
        ]
    )
    if not ruta:
        return

    entrada_texto.config(state="normal")
    entrada_texto.delete(0, tk.END)
    entrada_texto.insert(0, ruta)
    entrada_texto.config(state="readonly")

    df = load_data(ruta)
    if df is not None:
        mostrar_tabla(df)
        messagebox.showinfo("칄xito", "Datos cargados correctamente.")
        lanzar_selector(df)  # 游녣 aqu칤 se llama a tu interfaz de selecci칩n


